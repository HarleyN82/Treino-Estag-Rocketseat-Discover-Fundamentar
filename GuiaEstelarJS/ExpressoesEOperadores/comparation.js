console.log(one == "1")

//  !=  diferente a
console.log(two != 1)
console.log(one != "1")
console.log(one != 1)

console.log("\n")

// Operadores de Comparação (Valores e Tipos)
// Irá comparar valores e retornar um Boolean como resposta à compração

//  ===  igual a
console.log(two === 1)
console.log(one === 1)
console.log(one === "1")

//  !==  diferente a
console.log(two !== 1)
console.log(one !== "1")
console.log(one !== 1)

console.log("\n")

