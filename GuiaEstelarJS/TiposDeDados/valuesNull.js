/* 
    Undefined
        * Indefinido
        * Chave ou palavra reservada
        * Você declarou, mas não fez nada após
        * Não existe

    Null
        * Nulo
        * Objeto que não possui nada dentro
        * Você declarou e atribuiu que não teria nada
        * Existe, mas não tem nada dentro
    
    Empty 
        * Vazio
        * Você declarou, mas não tem nada dentro dele
        * Number = 0; String = ""; Array = [] e Object = {}
        * Existe, tem algo dentro de si e é vazio
    
    Obs: Declarar uma variável é reservar memória para armazenar uma informação.

*/

let number = 0 // empty
let food = null // null
let qualquerCoisa  // undefined

console.log(number)
console.log(food)
console.log(qualquerCoisa)